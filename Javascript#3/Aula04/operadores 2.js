//Relacionais
/*

> ---> maior
< ---> menor
>= ---> maior igual
<= ---> menor igual
== ---> igual
!= ---> não igual

*/
5 > 2 --> true
7 < 4 --> false
8 >= 8--> true
8 <= 7 --> false
5 == 5 --> true
4 != 4 --> false
5 == '5' --> true
5 === '5' --> false
// Logicos
/*

! ---> negaçao
&& ---> conjunçao E
|| ---> disjunçao OU

*/

//Primeiro
!true --> false
!false --> true
//Segundo
true && true --> true
true && false --> false
false && false --> false
false && true --> false
//Terceiro
true || true --> true
true || false --> true
false || true --> true
false || false -->false
